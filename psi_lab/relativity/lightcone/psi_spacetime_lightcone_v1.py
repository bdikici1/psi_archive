def _bar(value: float, width: int = 18, charset: str = "в–‘в–’в–“в–Ҳ") -> str:
    v = clamp(value, 0.0, 1.0)
    filled = int(round(v * width))
    return (charset[-1] * filled).ljust(width, "В·")

def _horizon_flag(psi_amp: float, psi_crit: float) -> str:
    return "HПҲ=ON" if psi_amp < psi_crit else "HПҲ=off"

